package Assignment1;

public class Task9 {

	public static void main(String[] args) {
		
		int num = 12;
		
		switch (num) {
		case 12:
			System.out.println("12");
			
		case 34:
			System.out.println("34");
			
		case 66:
			System.out.println("66");
			
		case 85:
			System.out.println("85");
			break;
		case 900:
			System.out.println("900");
			break;

		default:
			System.out.println("Number out of range");
			break;
		}
		
		

	}

}
